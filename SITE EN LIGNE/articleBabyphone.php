		</br></br>
		
		Nous avons choisi le nôtre dans un magasin de puériculture, c’est un basique (juste le son), dans la
		gamme de prix moyens. Il est constitué d’une base que tu branches dans la chambre de bébé avec un
		bouton on/off et une autre partie mobile qui se recharge. Cette partie comporte un bouton plus et
		moins pour le régler le volume, 3 petits voyants lumineux qui s’allument selon le son des pleurs de
		bébé (si les 3 voyants s’allument c’est que bébé hurle), et un autre voyant lumineux qui s’allume
		quand la partie mobile et la base sont bien connectées.
		</br></br>
		
		Après 3 mois de test, nous pouvons dire que pour nous (mais ça c’est personnel) ce fut un achat
		inutile mais que ce produit-là est en plus très décevant.
		La mise en route est très simple tu recharges la partie mobile. Une fois celle-ci rechargée, tu branches la
		base, tu appuis quelques secondes sur le bouton pour la mettre en marche,puis la même manipulation
		pour la partie mobile, et quand le voyant s’allume, c’est bon tu peux t’en servir!.
		</br></br>

		
		C’est là que les choses deviennent moins faciles: déjà la base doit être très très proche du lit de bébé
		(max 50 cm), car sinon elle ne détecte pas ses pleurs. Ensuite si la base et la partie mobile
		sont trop éloignées ça ne fonctionne pas (pourtant portée de 300 mètres annoncée).
		</br></br>
		
		Je veux bien que les murs de notre maison soit épais, mais dès que l’on dépassait 20 mètres il
		se mettait à biper, car oui quand la base et la partie mobile ne sont plus connectées, celle-ci émet un
		bip pour prévenir. Le genre de bip bien stressant qui réveille à tous les coups ton bébé (que tu as mis 30
		minutes à endormir),et qui fait que cet engin va bientôt passé par la fenêtre.
		</br></br>

		Autre point négatif, quand la partie mobile est en charge, le baby phone ne peut pas être utilisé.
		Seulement quand tu es jeune parent, que tu réfléchis déjà à 300 choses par, jour penser à mettre le
		baby-phone à charger ne te traverse pas forcément l’esprit, et si en plus tu es comme moi, que tu t’en sers la nuit
		parce que c’est aussi à ce moment-là que tu angoisses le plus de pas entendre ton bébé, c’est très
		très compliqué.
		</br></br>

		Nous ne vous conseillons donc pas d’acheter ce modèle. Et peut-être même d’attendre la naissance
